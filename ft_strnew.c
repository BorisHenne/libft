#include "libft.h"

char *ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	str = malloc(size * (sizeof(char)));
	i = 0;
	if (str == NULL)
		return (NULL);
	while (i <= size)
	{
		str[i] = '\0';
		i++;
	}
	return (str);
}
